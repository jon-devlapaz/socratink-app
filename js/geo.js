export function easeInOutCubic(t) { return t < 0.5 ? 4*t*t*t : 1-Math.pow(-2*t+2,3)/2; }
export function interpCoords(from, to, t) { return from.map((v,i)=>v+(to[i]-v)*t); }
export function coordsToPoints(arr) {
  const pts = [];
  for (let i=0; i<arr.length; i+=2) pts.push(arr[i]+','+arr[i+1]);
  return pts.join(' ');
}
